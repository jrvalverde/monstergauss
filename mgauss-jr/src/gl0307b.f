      R(3,2)=SINT*SINP
      R(3,3)=COST
      GO TO 15
    2 CONTINUE
      DO 6 I=1,3
      DO 7 J=1,3
    7 R(I,J)=ZERO
    6 R(I,I)=ONE
      GO TO 15
    4 CONTINUE
      SGN=PQZ/DABS(PQZ)
      DO 8 I=1,3
      DO 8 J=1,3
    8 R(I,J)=ZERO
      R(1,1)=ONE
      R(2,2)=SGN
