C?IBM/GLD/GBR/VAX/UNX
      SUBROUTINE DTWOL
C??
C?CDC
C     PROGRAM DTWOL
C??
C1INTRODUCTION
C     LINK 0703
C*
C     ----------------
C     GAUSSIAN 82
C     U OF T VERSION
C     FEBRUARY 1987
C     ----------------
C*
C     TWO ELECTRON FORCES ON THE NUCLEI FOR SP FUNCTIONS.
C
C     NOTE: THE 2-ELECTRON ENERGY COMPONENT (VEEXX) CANNOT BE EVALUATED
C     AS ALL INTEGRALS WHERE ALL 4 SHELLS ARE ON THE SAME CENTER
C     ARE SKIPPED.
C
C     AUTHOR: H.B. SCHLEGEL, QUEEN'S UNIVERSITY CHEMISTRY DEPT,
C     KINGSTON, ONTARIO, CANADA.
C     MODIFIED: MIKE PETERSON, U OF T CHEMISTRY DEPT, TORONTO, CANADA.
C*
C1OPTIONS
C     ******************************************************************
C     OPTIONS ... IOP()
C     ******************************************************************
C     SEE PROGRAM GBOPT ... LINK 0711.
C     ******************************************************************
C*
C/
      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
C#
C     PARAMETER (NA=#NA, NB=#NB)
C     PARAMETER (NS=#NS, NP=#NP)
C     PARAMETER (NF=#NF)
C##
      PARAMETER (NA= 36, NB=200)
      PARAMETER (NS=120, NP=300)
      PARAMETER (NF=120)
C###
      PARAMETER (NA3=3*NA)
      PARAMETER (NBB=NB*(NB+1)/2)
      PARAMETER (NF2=NF+2)
      PARAMETER (ZERO=0.0D0, ONE=1.0D0, TWO=2.0D0)
C
      INTEGER SHELLA,SHELLN,SHELLT,AOS,SHELLC
C
      LOGICAL FLAG,OPEN,IJSAME,KLSAME,IKSMJL,NOGSCF
C
      COMMON /A/ IOP(99)
      COMMON /A/ NATOMS,ICHARG,MULTIP,IAN(NA),NAE,NBE,NE,NBASIS,C(NA,3)
      COMMON /A/ CDUM(4),ICDUM(401)
C
      COMMON/C703A/SAVIMO(4),TXX,VNEXX,VEEXX,VNNXX,FN(NA3,6),
     1 FBEXX(NP,2),FBC1(NP,2),FBC2(NP,2)
      COMMON/FFQ703/FQ0,FQ1,FQ2,FQ3,FQ4,FQ5,AIJNDX(NB),
     1 XE34(100),E(256),
     2 TA0(400),TB0(400),TC0(400),TA1(400),TB1(400),TC1(400),
     3 TA2(400),TB2(400),TC2(400),TA3(400),TB3(400),TC3(400),
     4 TA4(400),TB4(400),TC4(400),TA5(400),TB5(400),TC5(400),
     5 QX(100),QY(100),QZ(100),TA34I(100),TS3(100),TS4(100),TS34(100),
     6 CSMCD(100)
      COMMON/DEN703/DM(NBB),DN(NBB)
      COMMON/B/IXYZ(NS),SHELLA(NS),SHELLN(NS),SHELLT(NS),
     1 SHELLC(NS),AOS(NS),NSHELL,MAXTYP,EXX(NP),C1(NP),C2(NP)
      COMMON /FP4/ QA,QA1,QA2,A12,A34I,A1234I,
     $ PQXX,PQYY,PQZZ,PQXY,PQXZ,PQYZ,
     *V0000,V0010,V0020,V0030,V0100,V0200,V0300,
     *V0110,V0120,V0130,V0210,V0220,V0230,V0310,V0320,V0330,
     *V1010,V1020,V1030,V2010,V2020,V2030,V3010,V3020,V3030,
     *V1000,V2000,V3000,V1100,V2100,V3100,V1200,V2200,V3200,
     *V1300,V2300,V3300,V1110,V2110,V3110,V1210,V2210,V3210,
     *V1310,V2310,V3310,V1120,V2120,V3120,V1220,V2220,V3220,
     *V1320,V2320,V3320,V1130,V2130,V3130,V1230,V2230,V3230,
     *V1330,V2330,V3330
      COMMON /FP4/ C1110,C2110,C3110,C1210,C2210,C3210,
     *C1320,C2320,C3320,C1130,C2130,C3130,C1230,C2230,C3230,
     *C1310,C2310,C3310,C1120,C2120,C3120,C1220,C2220,C3220,
     *C1330,C2330,C3330,OPXO,OPYO,OPZO,OPOX,OPOY,OPOZ,OPXOX,OPYOY,OPZOZ,
     *OPXX,OPXY,OPXZ,OPYX,OPYY,OPYZ,OPZX,OPZY,OPZZ,OQXO,OQYO,OQZO,
     *OQOX,OQOY,OQOZ,OQXOX,OQYOY,OQZOZ,OQXX,OQXY,OQXZ,OQYX,OQYY,OQYZ,
     *OQZX,OQZY,OQZZ
      COMMON /FP4/
     *GOOOO,GOOXO,GOOYO,GOOZO,GXOOO,GXOXO,GXOYO,GXOZO,GXXOO,GXXXO,GXXYO,
     *GXXZO,GXYOO,GXYZO,GXZOO,GYOOO,GYOYO,GYOZO,GYYOO,GYYXO,GYYYO,
     *GYYZO,GYZOO,GZOOO,GZOZO,GZZOO,GZZXO,GZZYO,GZZZO,
     *VE00,VE11,VE12,VE13,VE14,VE21,VE22,VE23,VE24,VE31,VE32,VE33,VE34,
     *CSSSP,CSSPP,CSPSP,CPSSP,CSPPP,CPSPP,CPPSP,CPPPP
      COMMON/FP4/VCC(NF,NF2)
C
      COMMON/IO/IN,IOUT,IODUM1(4),IFILE,IODUM2(10),NFILE(100,2)
C
      DIMENSION FXYZ(NA3),IJNDX(NB),IMOSAV(4),FQARRY(6)
C
      EQUIVALENCE (FXYZ(1),FN(1,4)), (SAVIMO(1),IMOSAV(1))
      EQUIVALENCE (IJNDX(1),AIJNDX(1)), (FQ0,FQARRY(1))
C
      DATA PI/3.14159265358979D0/, EXPCUT/80.0D0/
      DATA H/0.5D0/, TEN/10.0D0/
      DATA ONE5/1.5D0/, PT25/0.25D0/
      DATA TWENTY/20.0D0/
      DATA TWOPT5/2.5D0/, THRPT5/3.5D0/, FORPT5/4.5D0/
      DATA F16/16.0D0/, F19/19.0D0/, F60/60.0D0/, F100/100.0D0/
C
 1000 FORMAT ('0DTWOL: FMTGEN WAS CALLED',I9,' TIMES.')
 1010 FORMAT ('0*** FMTGEN FAILURE IN DTWOL FOR T =',1PD15.6)
C
      IF(IOP(10).EQ.0)RETURN
      PT78=PI*PT25
      PITO52=TWO*(PI**TWOPT5)
      CALL TREAD(3,TA0,400,18,400,18,0)
C     RECOVER DERIVATIVES FROM DISK.
      CALL TREAD (14, SAVIMO, NFILE(14,1), 1, NFILE(14,1), 1, 0)
C     PRESET F(M,T) ROUTINES.
      CALL FMTSET (0, 0, 0)
      IFMT = 0
C
C     SELECT CUTOFFS BASED ON IOP(20).
C
C...  *TEST* CUTOFFS ... DISABLE ALL CUTOFFS.
      IF (IOP(20) .EQ. 1) THEN
         VTOL1=TEN**(-30)
         VTOL2=TEN**(-30)
         VTOLS=TEN**(-30)
         FMT1=ZERO
         FMT2=F100
C...  "BERNY" ORIGINAL STO-3G CUTOFFS (QUICK BUT A LITTLE SLEAZY).
      ELSE IF (IOP(20) .EQ. 2) THEN
         VTOL1=TEN**(-11)
         VTOL2=TEN**(-9)
         VTOLS=TEN**(-14)
         FMT1=F16
         FMT2=ZERO
C...  STANDARD CUTOFFS (TRY TO COMPUTE INTEGRALS WITH 10**-8 ACC).
      ELSE
         VTOL1=TEN**(-12)
         VTOL2=TEN**(-12)
         VTOLS=TEN**(-20)
         FMT1=F19
         FMT2=F60
      END IF
C     SET INDEXING ARRAY.
      DO 1 I=1,NBASIS
    1 IJNDX(I)=(I*(I-1))/2
      NOGSCF=.TRUE.
      OPEN=.FALSE.
      J=(NBASIS*(NBASIS+1))/2
      IOP9=IOP(9)-1
      IF(IOP9)10,20,30
C     CLOSED SHELL.
   10 CALL TREAD(19,DM,NBB,1,J,1,0)
      GO TO 40
C     UHF OPEN SHELL.
   20 CALL TREAD(19,DM,NBB,1,J,1,0)
      CALL TREAD(21,DN,NBB,1,J,1,0)
      OPEN=.TRUE.
      GO TO 40
C     GENERALIZED SCF.
C     READ THE DENSITY MATRICES AND THE VECTOR COUPLING COEFFICIENTS.
   30 NVAL=IMOSAV(3)-IMOSAV(1)
      NOPEN=IMOSAV(4)-IMOSAV(3)
      NFOCK=NVAL+NOPEN+1
      K=J*NFOCK
      READ(IFILE)(DM(I),I=1,K)
      REWIND IFILE
      CALL TREAD(17,VCC,NF,NF2,NFOCK,NFOCK+2,0)
      NOGSCF=.FALSE.
C
C     BEGIN LOOPS OVER SHELLS.
C
   40 DO 1001 ISHELL=1,NSHELL
      IF(SHELLT(ISHELL).GT.1)GO TO 1001
C
      DO 1002 JSHELL=1,ISHELL
      IF(SHELLT(JSHELL).GT.1)GO TO 1002
C
      DO 1003 KSHELL=1,ISHELL
      IF(SHELLT(KSHELL).GT.1)GO TO 1003
      LMAX=KSHELL
      IF(ISHELL.EQ.KSHELL)LMAX=JSHELL
C
      DO 1004 LSHELL=1,LMAX
      IF(SHELLT(LSHELL).GT.1)GO TO 1004
      AX1=H
      IF(ISHELL.NE.JSHELL) AX1=AX1+AX1
      IF(KSHELL.NE.LSHELL) AX1=AX1+AX1
      IF(ISHELL.NE.KSHELL.OR.JSHELL.NE.LSHELL) AX1=AX1+AX1
      INEW=ISHELL
      JNEW=JSHELL
      KNEW=KSHELL
      LNEW=LSHELL
      LA=SHELLT(INEW)
      LB=SHELLT(JNEW)
      LC=SHELLT(KNEW)
      LD=SHELLT(LNEW)
C
C     PUT SHELLS IN STANDARD ORDER.
C
      IF(LA.GE.LB)GO TO 1012
      INEW=JSHELL
      JNEW=ISHELL
 1012 IF(LC.GE.LD)GO TO 1014
      KNEW=LSHELL
      LNEW=KSHELL
 1014 IF(LA+LB.GE.LC+LD)GO TO 1016
      ID=INEW
      INEW=KNEW
      KNEW=ID
      ID=JNEW
      JNEW=LNEW
      LNEW=ID
C     GET SOME SHELL INFORMATION.
 1016 IFQMAX=LA+LB+LC+LD+1
      IAT=IXYZ(INEW)
      JAT=IXYZ(JNEW)
      KAT=IXYZ(KNEW)
      LAT=IXYZ(LNEW)
      IF(IAT.EQ.JAT.AND.IAT.EQ.KAT.AND.IAT.EQ.LAT)GO TO 1004
      IJSAME=INEW.EQ.JNEW
      KLSAME=KNEW.EQ.LNEW
      IKSMJL=(INEW.EQ.KNEW).AND.(JNEW.EQ.LNEW)
      LA=3*SHELLT(INEW)+1
      LB=3*SHELLT(JNEW)+1
      LC=3*SHELLT(KNEW)+1
      LD=3*SHELLT(LNEW)+1
      JTYPE=(LA+LB+LC+LC+LD-2)/3
      AX=C(IAT,1)
      AY=C(IAT,2)
      AZ=C(IAT,3)
      IATX=3*(IAT-1)+1
      IATY=IATX+1
      IATZ=IATY+1
      BX=C(JAT,1)
      BY=C(JAT,2)
      BZ=C(JAT,3)
      JATX=3*(JAT-1)+1
      JATY=JATX+1
      JATZ=JATY+1
      CX=C(KAT,1)
      CY=C(KAT,2)
      CZ=C(KAT,3)
      KATX=3*(KAT-1)+1
      KATY=KATX+1
      KATZ=KATY+1
      DX=C(LAT,1)
      DY=C(LAT,2)
      DZ=C(LAT,3)
      LATX=3*(LAT-1)+1
      LATY=LATX+1
      LATZ=LATY+1
      ABX=AX-BX
      ABY=AY-BY
      ABZ=AZ-BZ
      CDX=CX-DX
      CDY=CY-DY
      CDZ=CZ-DZ
      R12=ABX**2+ABY**2+ABZ**2
      R34=CDX**2+CDY**2+CDZ**2
      ISTART=SHELLA(INEW)
      JSTART=SHELLA(JNEW)
      KSTART=SHELLA(KNEW)
      LSTART=SHELLA(LNEW)
      IEND=ISTART+SHELLN(INEW)-1
      JEND=JSTART+SHELLN(JNEW)-1
      KEND=KSTART+SHELLN(KNEW)-1
      LEND=LSTART+SHELLN(LNEW)-1
C     GET THE DENSITY MATRIX CONTRIBUTIONS.
      IAOS=AOS(INEW)-1
      JAOS=AOS(JNEW)-1
      KAOS=AOS(KNEW)-1
      LAOS=AOS(LNEW)-1
      DMAX=ZERO
      LA1=SHELLC(INEW)+1
      LB1=SHELLC(JNEW)+1
      LC1=SHELLC(KNEW)+1
      LD1=SHELLC(LNEW)+1
C
      DO 1020 I=1,256
 1020 E(I)=ZERO
C
      DO 1021 I=LA1,LA
      IAS=IAOS+I
      LII=IJNDX(IAS)
      DO 1021 J=LB1,LB
      JAS=JAOS+J
      LJJ=IJNDX(JAS)
      LIJ=LII+JAS
      IF(IAS.LT.JAS)LIJ=LJJ+IAS
      IF(NOGSCF)D12=DM(LIJ)
      IF(OPEN)D12B=DN(LIJ)
      DO 1021 K=LC1,LC
      KAS=KAOS+K
      LKK=IJNDX(KAS)
      LIK=LII+KAS
      IF(IAS.LT.KAS)LIK=LKK+IAS
      LJK=LJJ+KAS
      IF(JAS.LT.KAS)LJK=LKK+JAS
      IF(NOGSCF)D13=DM(LIK)
      IF(NOGSCF)D23=DM(LJK)
      IF(OPEN)D13B=DN(LIK)
      IF(OPEN)D23B=DN(LJK)
      DO 1021 L=LD1,LD
      LAS=LAOS+L
      LLL=IJNDX(LAS)
      LIL=LII+LAS
      IF(IAS.LT.LAS)LIL=LLL+IAS
      LJL=LJJ+LAS
      IF(JAS.LT.LAS)LJL=LLL+JAS
      LKL=LKK+LAS
      IF(KAS.LT.LAS)LKL=LLL+KAS
      ID=64*I+16*J+4*K+L-84
      IF(IOP9)1030,1040,1050
 1030 E(ID)=(D12*DM(LKL)-PT25*(D13*DM(LJL)+D23*DM(LIL)))*AX1
      GO TO 1060
 1040 E(ID)=((D12+D12B)*(DM(LKL)+DN(LKL))-H*(D13*DM(LJL)+
     $ D23*DM(LIL)+D13B*DN(LJL)+D23B*DN(LIL)))*AX1
      GO TO 1060
C
C     GENERAL SCF - OFFSET EACH INDEX BY THE NUMBER OF FOCK MATRICES.
C
 1050 NFIJ=LIJ*NFOCK-NFOCK
      NFIK=LIK*NFOCK-NFOCK
      NFIL=LIL*NFOCK-NFOCK
      NFJK=LJK*NFOCK-NFOCK
      NFJL=LJL*NFOCK-NFOCK
      NFKL=LKL*NFOCK-NFOCK
      D12=DM(NFIJ+1)
      D13=DM(NFIK+1)
      D14=DM(NFIL+1)
      D23=DM(NFJK+1)
      D24=DM(NFJL+1)
      D34=DM(NFKL+1)
C     CORE PART.
      D1234=D12*D34-PT25*(D13*D24+D14*D23)
      IF(NFOCK.EQ.1)GO TO 1056
      DO 1055 NI=2,NFOCK
      D12B=DM(NFIJ+NI)
      D13B=DM(NFIK+NI)
      D14B=DM(NFIL+NI)
      D23B=DM(NFJK+NI)
      D24B=DM(NFJL+NI)
      D34B=DM(NFKL+NI)
C     CORE - VALENCE-OPEN PART.
      D1234=D1234+H*VCC(NI,1)*(D12*D34B+D12B*D34)+
     $ PT25*VCC(1,NI)*(D13*D24B+D13B*D24+D14*D23B+D14B*D23)
C     VALENCE-OPEN - VALENCE-OPEN PART.
C     SELF-CONTRIBUTION.
      D1234=D1234+H*VCC(NI,NFOCK+1)*D12B*D34B+
     $ PT25*VCC(NI,NFOCK+2)*(D13B*D24B+D14B*D23B)
      IF(NI.EQ.2)GO TO 1055
      N=NI-1
      DO 1054 NJ=2,N
 1054 D1234=D1234+H*VCC(NI,NJ)*(D12B*DM(NFKL+NJ)+DM(NFIJ+NJ)*D34B) +
     $ PT25*VCC(NJ,NI)*(D13B*DM(NFJL+NJ)+DM(NFIK+NJ)*D24B+
     $ D14B*DM(NFJK+NJ)+DM(NFIL+NJ)*D23B)
 1055 CONTINUE
 1056 E(ID)=D1234*AX1
 1060 D1234=DABS(E(ID))
 1021 IF(D1234.GT.DMAX)DMAX=D1234
C
      IF(DMAX.LT.VTOL1)GO TO 1004
C     HUNT OUT THE LARGEST DENSITY*CONTRACTION AND REJECT IF POSSIBLE.
      E34MAX=ZERO
      KZERO=-10
      DO 2 K=KSTART,KEND
      KZERO=KZERO+10
      KL=KZERO
      A3=EXX(K)
      CSUMC=DABS(C1(K))+DABS(C2(K))
      LND=LEND
      IF(KLSAME)LND=K
      DO 2 L=LSTART,LND
      A4=EXX(L)
      KL=KL+1
      A34I=ONE/(A3+A4)
      S3=A3*A34I
      S4=A4*A34I
      S34=A3*S4
      QX(KL)=S3*CX+S4*DX
      QY(KL)=S3*CY+S4*DY
      QZ(KL)=S3*CZ+S4*DZ
      TA34I(KL)=A34I
      TS3(KL)=S3
      TS4(KL)=S4
      TS34(KL)=S34
      ARGEXP=R34*S34
      E34=ZERO
      IF(ARGEXP.LE.EXPCUT)E34=DEXP(-ARGEXP)*A34I
      IF(KLSAME.AND.(K.NE.L))E34=E34+E34
      XE34(KL)=E34
      E34=E34*CSUMC*(DABS(C1(L))+DABS(C2(L)))
      CSMCD(KL)=E34*E34
    2 IF(E34.GT.E34MAX)E34MAX=E34
C
      IF(DMAX*E34MAX.LT.VTOL1)GO TO 1004
      FXI=ZERO
      FXJ=ZERO
      FXK=ZERO
      FYI=ZERO
      FYJ=ZERO
      FYK=ZERO
      FZI=ZERO
      FZJ=ZERO
      FZK=ZERO
      VE11=ZERO
      VE12=ZERO
      VE13=ZERO
      VE14=ZERO
      VE21=ZERO
      VE22=ZERO
      VE23=ZERO
      VE24=ZERO
      VE31=ZERO
      VE32=ZERO
      VE33=ZERO
      VE34=ZERO
      IZERO=-10
C
C     LOOP OVER THE PRIMITIVES IN EACH SHELL.
C
      DO 2001 I=ISTART,IEND
      IZERO=IZERO+10
      IJ=IZERO
      A1=EXX(I)
      CSA=C1(I)
      CPA=C2(I)
      CSUMA=(DABS(CSA)+DABS(CPA))*DMAX
      JND=JEND
      IF(IJSAME)JND=I
C
      DO 2002 J=JSTART,JND
      IJ=IJ+1
      A2=EXX(J)
      A12=A1+A2
      A12I=ONE/A12
      S1=A1*A12I
      S2=A2*A12I
      S12=A1*S2
      E12=ZERO
      IF(R12*S12.LE.EXPCUT)E12=DEXP(-R12*S12)*PITO52*A12I
      IF(IJSAME.AND.(I.NE.J))E12=E12+E12
      CSB=C1(J)*E12
      CPB=C2(J)*E12
      CSMAB=CSUMA*(DABS(CSB)+DABS(CPB))
      IF(CSMAB*E34MAX.LT.VTOL2)GO TO 2002
      CSMAB=CSMAB*CSMAB
      CSS=CSA*CSB
      CPS=CPA*CSB
      CSP=CSA*CPB
      CPP=CPA*CPB
      PX=S1*AX+S2*BX
      PY=S1*AY+S2*BY
      PZ=S1*AZ+S2*BZ
      IF(LA.EQ.1)GO TO 51
      OPXO=-S2*ABX
      OPYO=-S2*ABY
      OPZO=-S2*ABZ
      IF(LB.EQ.1)GO TO 51
      OPOX=S1*ABX
      OPOY=S1*ABY
      OPOZ=S1*ABZ
      OPXOX=OPXO+OPOX
      OPYOY=OPYO+OPOY
      OPZOZ=OPZO+OPOZ
      HA12I=H*A12I
      OPXX=OPXO*OPOX+HA12I
      OPYY=OPYO*OPOY+HA12I
      OPZZ=OPZO*OPOZ+HA12I
      OPXY=OPXO*OPOY
      OPYX=OPXY
      OPXZ=OPXO*OPOZ
      OPZX=OPXZ
      OPYZ=OPYO*OPOZ
      OPZY=OPYZ
C
   51 VE00S=ZERO
      VE11S=ZERO
      VE12S=ZERO
      VE21S=ZERO
      VE22S=ZERO
      VE31S=ZERO
      VE32S=ZERO
      DVEXS=ZERO
      DVEYS=ZERO
      DVEZS=ZERO
      KND=KEND
      IF(IKSMJL)KND=I
      KZERO=-10
C
      DO 2003 K=KSTART,KND
      KZERO=KZERO+10
      KL=KZERO
      A3=EXX(K)
      CSC=C1(K)
      CPC=C2(K)
      CSSS=CSS*CSC
      CSSP=CSS*CPC
      CPSS=CPS*CSC
      CPSP=CPS*CPC
      CSPS=CSP*CSC
      CSPP=CSP*CPC
      CPPS=CPP*CSC
      CPPP=CPP*CPC
      LND=LEND
      IF(KLSAME)LND=K
      IF(IKSMJL.AND.(I.EQ.K))LND=J
C
      DO 2004 L=LSTART,LND
      KL=KL+1
      A34=A3+EXX(L)
      A1234I=ONE/(A12+A34)
      VTEST=CSMAB*CSMCD(KL)*A1234I
      IF(VTEST.LT.VTOLS)GO TO 2004
      QA2=A34*A1234I
      QA=A12*QA2
      PQX=PX-QX(KL)
      PQY=PY-QY(KL)
      PQZ=PZ-QZ(KL)
      PQXX=PQX*PQX
      PQYY=PQY*PQY
      PQZZ=PQZ*PQZ
      T=QA*(PQXX+PQYY+PQZZ)
      A34I=TA34I(KL)
      S3=TS3(KL)
      S4=TS4(KL)
      S34=TS34(KL)
      E34=XE34(KL)*DSQRT(A1234I)
      IF(IKSMJL.AND.(IJ.NE.KL))E34=E34+E34
      CSD=C1(L)*E34
      EOOOO=E(1)*CSSS*CSD
      IF(T.LE.FMT1)GO TO 100
      IF(T.LE.FMT2)GO TO 120
      TI=ONE/T
      IF(VTEST*TI.LT.VTOLS)GO TO 2004
      FQ0=DSQRT(PT78*TI)
      FQ1=H*FQ0*TI
      IF(JTYPE.EQ.1)GO TO 3003
      FQ2=ONE5*FQ1*TI
      IF(JTYPE.EQ.2)GO TO 3004
      FQ3=TWOPT5*FQ2*TI
      FQ4=THRPT5*FQ3*TI
      FQ5=FORPT5*FQ4*TI
      GO TO 110
C
  100 QQ=T*TWENTY
